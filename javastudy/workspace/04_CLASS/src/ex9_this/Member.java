package ex9_this;

public class Member {

	// this
	// 현재 객체를 의미함
	// this. : 현재 클래스의 field/method를 의미함
	
	private String id;
	private String password;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	
	
	
}







